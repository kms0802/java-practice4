package For_while_dowhile.for_while_dowhile_challenges;

public class for_challenge_1 {
    public static void main(String[]args){
        int i, j;
        for (i = 1; i <= 9; i++){
            for (j = 1; j <= 9; j++){
                System.out.println(i + "x" + j + "=" + (i * j));
            }
        }
    }
    
}
